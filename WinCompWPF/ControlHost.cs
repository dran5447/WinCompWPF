    /*
     * Host for Composition content
     */ 
    class ControlHost: HwndHost
    {
        IntPtr hwndHost;
        int hostHeight, hostWidth;
        object dispatcherQueue;
        private ContainerVisual mainContainer;
        private Compositor c;
        private BarGraph currentGraph;

        internal const int
          WS_CHILD = 0x40000000,
          WS_VISIBLE = 0x10000000,
          LBS_NOTIFY = 0x00000001,
          HOST_ID = 0x00000002,
          LISTBOX_ID = 0x00000001,
          WS_VSCROLL = 0x00200000,
          WS_BORDER = 0x00800000;

        public ControlHost(double height, double width)
        {
            hostHeight = (int)height;
            hostWidth = (int)width;
        }
        
        protected override HandleRef BuildWindowCore(HandleRef hwndParent)
        {
            // Create Window
            hwndHost = IntPtr.Zero;
            hwndHost = CreateWindowEx(0, "static", "",
                                      WS_CHILD | WS_VISIBLE,
                                      0, 0,
                                      hostWidth, hostHeight,
                                      hwndParent.Handle,
                                      (IntPtr)HOST_ID,
                                      IntPtr.Zero,
                                      0);

            // Create Dispatcher Queue
            dispatcherQueue = InitializeCoreDispatcher();

            // Build Composition Tree of content
            InitComposition(hwndHost);

            return new HandleRef(this, hwndHost);
        }

        private object InitializeCoreDispatcher()
        {
            DispatcherQueueOptions options = new DispatcherQueueOptions();
            options.apartmentType = DISPATCHERQUEUE_THREAD_APARTMENTTYPE.DQTAT_COM_STA;
            options.threadType = DISPATCHERQUEUE_THREAD_TYPE.DQTYPE_THREAD_CURRENT;
            options.dwSize = Marshal.SizeOf(typeof(DispatcherQueueOptions));

            object queue = null;
            CreateDispatcherQueueController(options, out queue);
            return queue;
        }

        private void InitComposition(IntPtr hwndHost)
        {
            ICompositorDesktopInterop interop;

            c = new Compositor();
            object iunknown = c as object;
            interop = (ICompositorDesktopInterop)iunknown;
            IntPtr raw;
            interop.CreateDesktopWindowTarget(hwndHost, true, out raw);

            object rawObject = Marshal.GetObjectForIUnknown(raw);
            ICompositionTarget target = (ICompositionTarget)rawObject;

            if (raw == null) { throw new Exception("QI Failed"); } //TODO: cleanup

            mainContainer = c.CreateContainerVisual();
            target.Root = mainContainer;
        }

        public void UpdateGraph(Customer customer)
        {
            var graphTitle = customer.FirstName + " Investment History";
            //TODO fix labels
            var xAxisTitle = "Investment #"; 
            var yAxisTitle = "# Shares of Stock";

            //TODO update how data is passed and retrieved to graph (need to be able to include value + label for each bar in dataset)
            //TODO get graph size from parent container size?


            // If graph already exists update values. Else create new graph.
            if (mainContainer.Children.Count > 0 && currentGraph!=null)
            {
                currentGraph.UpdateGraphData(graphTitle, xAxisTitle, yAxisTitle, customer.Data);
            }
            else
            {
                BarGraph graph = new BarGraph(c, hwndHost, graphTitle, xAxisTitle, yAxisTitle, hostWidth, hostHeight, customer.Data, true, BarGraph.GraphOrientation.Vertical, BarGraph.GraphBarColorOptions.PerBarLinearGradient);

                currentGraph = graph;
                mainContainer.Children.InsertAtTop(graph.GraphRoot);                
            }
        }

        public void UpdateGraphSize(float width, float height)
        {

        }


        protected override void DestroyWindowCore(HandleRef hwnd)
        {
            DestroyWindow(hwnd.Handle);
        }


